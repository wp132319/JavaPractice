package org.dimigo.oop;

public class Car {
	private String company;
	private String model;
	private String color;
	private int    maxSpeed;
	private int    price;
	
	public String getCompany() {
		return company;
	}
	public String getModel() {
		return model;
	}
	public String getColor() {
		return color;
	}
	public int getMaxSpeed() {
		return maxSpeed;
	}
	public int getPrice() {
		return price;
	}
	public void setCompany(String getCompany) {
		company = getCompany;
	}
	public void setModel(String getModel) {
		model = getModel;
	}
	public void setColor(String getColor) {
		color = getColor;
	}
	public void setMaxSpeed(int getMaxSpeed) {
		maxSpeed = getMaxSpeed;
	}
	public void setPrice(int getPrice) {
		price = getPrice;
	}
	
	
}
